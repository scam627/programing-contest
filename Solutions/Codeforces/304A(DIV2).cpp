#include <bits/stdc++.h>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    long long deuda;
    int n, k, w;
    cin >> n >> k >> w;
    deuda = ((w * w + w) / 2) * n - k;
    if (deuda > 0)
        cout << deuda << "\n";
    else
        cout << 0 << "\n";
    return 0;
}
